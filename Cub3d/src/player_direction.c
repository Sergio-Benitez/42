#include "cub3d.h"

static void	set_north_direction(t_game *game)
{
	game->player.dir_x = 0;
	game->player.dir_y = -1;
	game->player.plane_x = 0.66;
	game->player.plane_y = 0;
}

static void	set_south_direction(t_game *game)
{
	game->player.dir_x = 0;
	game->player.dir_y = 1;
	game->player.plane_x = -0.66;
	game->player.plane_y = 0;
}

static void	set_east_west_direction(t_game *game, char direction)
{
	if (direction == 'E')
	{
		game->player.dir_x = 1;
		game->player.dir_y = 0;
		game->player.plane_x = 0;
		game->player.plane_y = 0.66;
	}
	else if (direction == 'W')
	{
		game->player.dir_x = -1;
		game->player.dir_y = 0;
		game->player.plane_x = 0;
		game->player.plane_y = -0.66;
	}
}

void	ft_set_player_direction(t_game *game, char direction)
{
	if (direction == 'N')
		set_north_direction(game);
	else if (direction == 'S')
		set_south_direction(game);
	else
		set_east_west_direction(game, direction);
}
